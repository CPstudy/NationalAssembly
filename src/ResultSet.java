
public class ResultSet {
	String title;
	String[] article;
	String filename;
	float score;
	String exp;
	
	ResultSet(String title, String[] article, String filename, float score, String exp) {
		this.title = title;
		this.article = article;
		this.filename = filename;
		this.score = score;
		this.exp = exp;
	}
}
