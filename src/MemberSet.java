
public class MemberSet {
	String korean;
	String chinese;
	String english;
	String num;
	String election;
	String locale;
	
	MemberSet(String korean, String chinese, String english, String num, String election, String locale) {
		this.korean = korean;
		this.chinese = chinese;
		this.english = english;
		this.num = num;
		this.election = election;
		this.locale = locale;
	}
}
